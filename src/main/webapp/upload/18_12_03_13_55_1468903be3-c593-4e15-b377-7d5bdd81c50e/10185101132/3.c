void laceString( /* in*/ const char a[],
                         const char b[],
                 /*out*/ char s[] )
{
    int i;
    int cnt = 0;
    for(i = 0; i < 85; i++){
        if(a[i] != 0 && b[i] != 0){
            s[2 * i] = a[i];
            s[2 * i + 1] = b[i];
            cnt += 2;
        }else if(a[i] != 0 && b[i] == 0){
            s[cnt++] = a[i];
        }else if(a[i] == 0 && b[i] != 0){
            s[cnt++] = b[i];
        }else{
            break;
        }
    }
}
