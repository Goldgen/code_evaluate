#include <stdio.h>
#include <stdlib.h>
int main()
{
    int a,b,c,d,e,f,g,h,He;
    scanf ("%d%d%d",&a,&b,&c);
    scanf ("%d%d%d",&d,&e,&f);
    scanf ("%d%d",&g,&h);
    He=a+b+c+d+e+f+g+h;
    printf  ("%d\n",He);

    int i,j,k,l,m,n,o,p,HE;
    scanf ("%d,%d,%d",&i,&j,&k);
    scanf ("%d,%d,%d",&l,&m,&n);
    scanf ("%d,%d",&o,&p);
    HE=i+j+k+l+m+n+o+p;
    printf ("%d",HE);
    return 0;
}
